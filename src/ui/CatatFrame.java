    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            java.io.FileWriter writer = new java.io.FileWriter("catatan.txt");

            for (int i = 0; i < listModel.size(); i++) {
                Catatan c = listModel.get(i);

                writer.write(c.getJudul() + "\n");
                writer.write("---\n");
                writer.write(c.getIsi() + "\n");
                writer.write("===\n");
            }

            writer.close();
            javax.swing.JOptionPane.showMessageDialog(this,
                "Data berhasil disimpan ke catatan.txt");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        // TODO add your handling code here:
        try {
            java.io.File file = new java.io.File("catatan.txt");
            if (!file.exists()) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "File catatan.txt tidak ditemukan!");
                return;
            }

            java.util.Scanner sc = new java.util.Scanner(file);

            listModel.clear();
            manager.clear(); // tambahkan fungsi ini di manager

            while (sc.hasNextLine()) {
                String judul = sc.nextLine();
                sc.nextLine(); // lewati baris ---
                StringBuilder isi = new StringBuilder();

                String line;
                while (!(line = sc.nextLine()).equals("===")) {
                    isi.append(line).append("\n");
                }

                Catatan c = new Catatan(judul, isi.toString().trim());
                listModel.addElement(c);
                manager.tambah(c);
            }

            sc.close();

            javax.swing.JOptionPane.showMessageDialog(this,
                "Data berhasil dimuat!");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Gagal memuat: " + e.getMessage());
        }
    }//GEN-LAST:event_btnLoadActionPerformed


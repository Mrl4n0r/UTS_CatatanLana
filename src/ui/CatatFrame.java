    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int index = listCatatan.getSelectedIndex();

        if (index == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih catatan yang ingin dihapus!");
            return;
        }

        // Konfirmasi sebelum hapus
        int yakin = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus catatan?",
            "Konfirmasi Hapus",
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (yakin != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        // Ambil data
        Catatan dipilih = listModel.getElementAt(index);

        // Hapus dari Manager
        manager.hapus(dipilih);

        // Hapus dari ListModel
        listModel.remove(index);

        // Bersihkan input
        txtJudul.setText("");
        txtIsi.setText("");

        javax.swing.JOptionPane.showMessageDialog(this,
            "Catatan berhasil dihapus!");
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int index = listCatatan.getSelectedIndex();

        if (index == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih catatan yang ingin diedit!");
            return;
        }

        String judulBaru = txtJudul.getText().trim();
        String isiBaru = txtIsi.getText().trim();

        if (judulBaru.isEmpty() || isiBaru.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Judul dan isi tidak boleh kosong!");
            return;
        }

        // Ambil catatan yang sedang dipilih
        Catatan c = listModel.getElementAt(index);

        // Update data
        c.setJudul(judulBaru);
        c.setIsi(isiBaru);

        // Refresh tampilan list
        listModel.setElementAt(c, index);

        javax.swing.JOptionPane.showMessageDialog(this,
            "Catatan berhasil diperbarui!");
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String judul = txtJudul.getText();
        String isi = txtIsi.getText();

        if(judul.isEmpty() || isi.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this,
                "Judul dan isi tidak boleh kosong!");
            return;
        }

        // 1. Buat objek Catatan baru
        Catatan catatanBaru = new Catatan(judul, isi);

        // 2. Simpan ke manager
        manager.tambah(catatanBaru);

        // 3. Tambah ke listModel supaya tampil di JList
        listModel.addElement(catatanBaru);

        // 4. Bersihkan input
        txtJudul.setText("");
        txtIsi.setText("");

        // 5. Pesan sukses
        javax.swing.JOptionPane.showMessageDialog(this,
            "Catatan berhasil ditambahkan!");
    }//GEN-LAST:event_btnTambahActionPerformed

